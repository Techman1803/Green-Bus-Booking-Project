    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        try {
            // TODO add your handling code here:
            
            DefaultTableModel dft1 = (DefaultTableModel)jTable1.getModel();
            int selected = jTable1.getSelectedRow();
            
            String busno = dft1.getValueAt(selected, 0).toString();
            
            
            String name = txtname.getText();
            String seat = txtseatno.getText();
            String Mobile = txtmobile.getText();
            String Date = txtdate.getText();
            
            pstm = con.prepareStatement("insert into bookings(busno,seat,customer,mobile,date)values(?,?,?,?,?)");
            pstm.setString(1, busno);
            pstm.setString(2, seat);
            pstm.setString(3, name);
            pstm.setString(4, Mobile);
            pstm.setString(5, Date);
            
            pstm.executeUpdate();
            
            
            pstm2 = con.prepareStatement("update seats set status = ? where seat = ?");
            pstm2.setString(1, "Booked...");
            pstm2.setString(2, seat);
            pstm2.executeUpdate();
            
            JOptionPane.showMessageDialog(this, "Bus has been Bookedddd!!!");
            Load();
            
            txtname.setText("");
            txtseatno.setText("");
            txtmobile.setText("");
            txtdate.setText("");
            
            
        } catch (SQLException ex) {
            Logger.getLogger(BusBooking.class.getName()).log(Level.SEVERE, null, ex);
        }
        
        
        
    }//GEN-LAST:event_jButton2ActionPerformed

    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked
        // TODO add your handling code here:
        
        DefaultTableModel dft1 = (DefaultTableModel)jTable1.getModel();
        int selected = jTable1.getSelectedRow();
        
        String status = dft1.getValueAt(selected, 2).toString();
        
        if(!status.equals("Booked..."))
        {
            String seat = dft1.getValueAt(selected, 1).toString();
            String date = dft1.getValueAt(selected, 5).toString();
            
            txtseatno.setText(seat);
            txtdate.setText(date);
            
        }
        else 
        {
            JOptionPane.showMessageDialog(this, "Ticket has been already Booked");
        }
        
    }//GEN-LAST:event_jTable1MouseClicked

