    public void Load()
    {
        
        try {
            // TODO add your handling code here:
            
            SimpleDateFormat Dt = new SimpleDateFormat("YYYY-MM-DD");
            String Date = Dt.format(txtdchooser.getDate());
            
            pstm = con.prepareStatement("Select seats.busno,seats.seat,seats.status,seats.date,bookings.customer,bookings.mobile from seats left join bookings on seats.busno = bookings.busno and seats.seat = bookings.seat and seats.date = bookings.date where seats.date = ?");
            pstm.setString(1, Date);
            rs = pstm.executeQuery();
            
            ResultSetMetaData rsd = rs.getMetaData();
            int c;
            c = rsd.getColumnCount();
            DefaultTableModel dft = (DefaultTableModel)jTable1.getModel();
            dft.setRowCount(0);
            
            while(rs.next())
            {
                Vector v2 = new Vector();
                
                for(int i=1;i<c;i++)
                {
                    v2.add(rs.getString("busno"));
                    v2.add(rs.getString("seat"));
                    v2.add(rs.getString("status"));
                    v2.add(rs.getString("customer"));
                    v2.add(rs.getString("mobile"));
                    v2.add(rs.getString("date"));
                    
                }
                dft.addRow(v2);
            }

        } catch (SQLException ex) {
            Logger.getLogger(BusBooking.class.getName()).log(Level.SEVERE, null, ex);
        }
        
    }
    
    
    

